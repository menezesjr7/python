frase = 'Curso em Vídeo Python'
print('String inteira:', frase)
print('Fatiando uma posição específica da string:', frase[9])
print('Fatiando um trecho específico da string:', frase[6:15])
print('Fatiando um trecho específico do começo da string:', frase[:15])
print('Fatiando um trecho específico do final da string:', frase[9:])
print('Fatiando um trecho específico e pulando de dois em dois:', frase[:17:2])
print('String toda, pulando de três em três:', frase[::3])